  private void b_bbddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_bbddActionPerformed
    
    //pillar datos de un txt separados por ,
    DefaultTableModel model = (DefaultTableModel) t_tabla.getModel();    
    String line;
        String path = "archivo.txt";
        try (BufferedReader reader = new BufferedReader(new FileReader(path))) {
            while ((line = reader.readLine()) != null) {
                String[] data = line.split(",");
                if (data.length == 2) {
                    String nombre = data[0].trim();
                    String apellido = data[1].trim();
                    Persona persona = new Persona(nombre, apellido);
                    listaPersonas.add(persona);
                    
                    model.addRow(new Object[]{nombre, apellido});
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        } 
  }//GEN-LAST:event_b_bbddActionPerformed

